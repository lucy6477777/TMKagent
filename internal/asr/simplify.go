// SimplifyZh converts common Traditional Chinese characters to Simplified Chinese.
// Safe to call on already-simplified text (no-op for characters not in the map).
// Only intended for zh source language.
func SimplifyZh(s string) string {
	runes := []rune(s)
	for i, r := range runes {
		if simp, ok := tradToSimp[r]; ok {
			runes[i] = simp
		}
	}
	return string(runes)
}

